const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },

    email: {
      type: String,
      required: true,
      unique: true,
    },

    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: { createdAt: "", updatedAt: "" } }
);

const userModel = new mongoose.model("userModel", userSchema);

module.exports = userModel;
